package com.example.todoapp1;

public class MainData {

    private int id;
    private String tv_todo, tv_memo, tv_writeDate;

    public MainData() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTv_todo() {
        return tv_todo;
    }

    public void setTv_todo(String tv_todo) {
        this.tv_todo = tv_todo;
    }

    public String getTv_memo() {
        return tv_memo;
    }

    public void setTv_memo(String tv_memo) {
        this.tv_memo = tv_memo;
    }

    public String getTv_writeDate() {
        return tv_writeDate;
    }

    public void setTv_writeDate(String tv_writeDate) {
        this.tv_writeDate = tv_writeDate;
    }
}
